# Hàm tạo Vector Đặc trưng (State/Observation) để cung cấp cho RL Agent
def build_state_features(item: Dict[str, Any]) -> np.ndarray:
    q = item["query"]
    feats = []
    # Đưa các điểm số: tương đồng, từ khóa, khớp thực thể/thuộc tính và điểm xếp hạng (1/(rank+1)) vào mảng
    for c in item["candidates"]:
        feats.extend([
            c["sim"],
            c["overlap"],
            c["entity_match"],
            c["slot_match"],
            1.0 / (1.0 + c["rank"]),
        ])
    # Bổ sung thông tin ngữ cảnh chung của câu hỏi
    unique_topic_bonus = 1.0 if q["topic"] in q["query"].lower() else 0.0
    query_len = min(len(q["query"].split()) / 20.0, 1.0)
    feats.extend([unique_topic_bonus, query_len])
    
    return np.array(feats, dtype=np.float32)

