# Định nghĩa Môi trường Học Tăng Cường theo chuẩn của Gymnasium
class MemoryRetrievalEnv(gym.Env):
    metadata = {"render_modes": ["human"]}

    def __init__(self, candidate_items: List[Dict[str, Any]], seed: int = 42):
        super().__init__()
        self.candidate_items = candidate_items
        self.rng = np.random.default_rng(seed)
        # Định nghĩa không gian quan sát (State) và không gian hành động (Action)
        self.observation_space = spaces.Box(low=-10, high=10, shape=(STATE_DIM,), dtype=np.float32)
        self.action_space = spaces.Discrete(NUM_ACTIONS)
        self.current = None

    # Hàm reset: Bắt đầu một vòng lặp mới bằng cách chọn ngẫu nhiên một câu hỏi
    def reset(self, seed=None, options=None):
        if seed is not None:
            self.rng = np.random.default_rng(seed)
        idx = int(self.rng.integers(0, len(self.candidate_items)))
        self.current = self.candidate_items[idx]
        obs = build_state_features(self.current)
        info = {"query_id": self.current["query"]["query_id"]}
        return obs, info

    # Hàm step: Agent thực hiện một hành động (chọn trí nhớ) và nhận lại Phần Thưởng (Reward)
    def step(self, action):
        chosen = self.current["candidates"][int(action)]
        q = self.current["query"]

        # Hệ thống Phần Thưởng (Reward System)
        # Đây là phần cốt lõi điều chỉnh hành vi của Agent. Thưởng lớn nhất nếu chọn đúng trí nhớ (is_gold).
        # Ngoài ra thưởng thêm nếu các yếu tố phụ trợ khớp, và phạt (trừ điểm) nếu chọn ứng viên xếp hạng thấp.
        reward = 0.0
        reward += 2.0 * chosen["is_gold"]
        reward += 0.8 * chosen["entity_match"]
        reward += 0.6 * chosen["slot_match"]
        reward += 0.5 * chosen["sim"]
        reward += 0.3 * chosen["overlap"]
        reward -= 0.15 * chosen["rank"]

        # Hoàn tất vòng lặp ngay sau khi chọn xong (One-step episode)
        done = True
        truncated = False
        info = {
            "query_id": q["query_id"],
            "chosen_memory_id": chosen["memory_id"],
            "gold_memory_id": q["gold_memory_id"],
            "chosen_text": chosen["text"],
            "gold_text": q["gold_text"],
            "is_correct": bool(chosen["memory_id"] == q["gold_memory_id"]),
            "gold_value": q["gold_value"],
            "query": q["query"],
        }
        # Trả về Mảng trạng thái tiếp theo (bằng 0 vì episode đã kết thúc), phần thưởng, v.v.
        next_obs = np.zeros(self.observation_space.shape, dtype=np.float32)
        return next_obs, float(reward), done, truncated, info

