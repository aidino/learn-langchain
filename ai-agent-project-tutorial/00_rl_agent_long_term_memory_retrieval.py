# Hàm tính toán độ trùng lặp từ khóa (overlap) giữa câu hỏi và trí nhớ
def keyword_overlap(a: str, b: str) -> float:
    # Xóa dấu phẩy, chấm hỏi, chuyển thành chữ thường và tách từ
    ta = set(a.lower().replace("?", "").replace(".", "").split())
    tb = set(b.lower().replace("?", "").replace(".", "").split())
    if not ta or not tb:
        return 0.0
    # Tính tỷ lệ (số từ chung) / (tổng số từ độc lập)
    return len(ta & tb) / max(1, len(ta | tb))

# Hàm tìm Top 8 trí nhớ giống với câu hỏi nhất dựa vào độ tương đồng vector (Cosine Similarity)
def get_top_k_candidates(query_idx: int, k: int = 8) -> Dict[str, Any]:
    qv = query_embeddings[query_idx:query_idx+1]
    # Tính cosine similarity giữa vector câu hỏi và toàn bộ vector trong kho trí nhớ
    sims = cosine_similarity(qv, memory_embeddings)[0]
    # Lấy ra index của top K ứng viên có độ tương đồng cao nhất
    top_idx = np.argsort(-sims)[:k]
    
    candidates = []
    q = queries[query_idx]
    # Tính toán thêm các đặc trưng phụ trợ cho từng ứng viên
    for rank, midx in enumerate(top_idx):
        mem = memory_bank[midx]
        sim = float(sims[midx])
        overlap = keyword_overlap(q["query"], mem.text)
        # Xem tên thực thể và thuộc tính có khớp đúng trong chữ không
        entity_match = 1.0 if q["entity"].lower() in mem.text.lower() else 0.0
        slot_match = 1.0 if q["slot"].replace("_", " ").lower() in mem.text.lower() else 0.0
        # Xác định xem đây có phải là mảnh trí nhớ chuẩn xác không
        is_gold = 1.0 if mem.memory_id == q["gold_memory_id"] else 0.0
        
        candidates.append({
            "rank": rank,
            "memory_index": midx,
            "memory_id": mem.memory_id,
            "text": mem.text,
            "sim": sim,
            "overlap": overlap,
            "entity_match": entity_match,
            "slot_match": slot_match,
            "is_gold": is_gold,
        })
    return {"query": q, "candidates": candidates}

