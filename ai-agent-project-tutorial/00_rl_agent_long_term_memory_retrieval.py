def interactive_demo(question: str, top_k: int = 8):
    qv = embed_texts([question])
    sims = cosine_similarity(qv, memory_embeddings)[0]
    top_idx = np.argsort(-sims)[:top_k]

    candidates = []
    for rank, midx in enumerate(top_idx):
        mem = memory_bank[midx]
        candidates.append({
            "rank": rank,
            "memory_index": int(midx),
            "memory_id": int(mem.memory_id),
            "text": mem.text,
            "sim": float(sims[midx]),
            "overlap": keyword_overlap(question, mem.text),
            "entity_match": 0.0,
            "slot_match": 0.0,
            "is_gold": 0.0,
        })

    pseudo_item = {
        "query": {
            "query_id": -1,
            "query": question,
            "entity": "unknown",
            "slot": "unknown",
            "gold_value": "unknown",
            "gold_memory_id": -1,
            "gold_text": "",
            "topic": "unknown",
        },
        "candidates": candidates,
    }

    # RL Agent phân tích Mảng trạng thái và quyết định chọn trí nhớ tốt nhất
    obs = build_state_features(pseudo_item)
    action, _ = model.predict(obs, deterministic=True)
    selected = pseudo_item["candidates"][int(action)]
    answer = chat_answer(question, [{"memory_id": selected["memory_id"], "text": selected["text"]}])

    # In kết quả trực quan
    print("=" * 100)
    print("QUESTION")
    print(question)
    print("=" * 100)
    print("TOP CANDIDATES")
    for c in candidates:
        print(f"[Rank {c['rank']}] sim={c['sim']:.4f} | {c['text']}")
    print("=" * 100)
    print("RL-SELECTED MEMORY")
    print(selected["text"])
    print("=" * 100)
    print("ANSWER")
    print(answer)
    print("=" * 100)

