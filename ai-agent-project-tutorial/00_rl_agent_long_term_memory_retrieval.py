# Hàm chia nhỏ danh sách để xử lý theo batch (giúp gọi API hiệu quả hơn)
def chunked(xs, n):
    for i in range(0, len(xs), n):
        yield xs[i:i+n]

# Hàm chuyển đổi văn bản thành vector nhúng (embeddings)
def embed_texts(texts: List[str], model: str = EMBED_MODEL, batch_size: int = 64) -> np.ndarray:
    outputs = []
    # Xử lý theo từng batch để không bị lỗi giới hạn độ dài của API
    for batch in tqdm(list(chunked(texts, batch_size)), desc="Embedding"):
        resp = client.embeddings.create(model=model, input=batch)
        batch_vecs = [d.embedding for d in resp.data]
        outputs.extend(batch_vecs)
    arr = np.array(outputs, dtype=np.float32)
    # Chuẩn hóa vector (L2 normalization) để tính toán độ tương đồng Cosine dễ dàng hơn
    norms = np.linalg.norm(arr, axis=1, keepdims=True) + 1e-12
    arr = arr / norms
    return arr

# Hàm dùng LLM để trả lời câu hỏi dựa trên trí nhớ đã được cung cấp (RAG cơ bản)
def chat_answer(question: str, retrieved_memories: List[Dict[str, Any]], model: str = CHAT_MODEL) -> str:
    # Ghép các trí nhớ lại thành một chuỗi văn bản
    memory_block = "\n".join([f"[Memory {i+1}] {m['text']}" for i, m in enumerate(retrieved_memories)])
    # Ép LLM chỉ được phép trả lời dựa trên những trí nhớ được cung cấp
    system = "You are a precise QA assistant. Answer the question using only the provided memories. If the memories do not contain the answer, say 'I do not know from the provided memories.'"
    user = f"Question: {question}\n\nRetrieved memories:\n{memory_block}\n\nAnswer:"
    resp = client.chat.completions.create(
        model=model,
        temperature=0, # Temperature = 0 để câu trả lời có tính xác định cao nhất
        messages=[
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
    )
    return resp.choices[0].message.content.strip()

# Hàm dùng LLM như một "giám khảo" để tự động chấm điểm xem câu trả lời có khớp đáp án chuẩn không
def llm_judge_exact(question: str, gold_answer: str, predicted_answer: str, model: str = CHAT_MODEL) -> float:
    # LLM Giám khảo trả về chuẩn JSON với 1 key 'score'
    system = "You are a strict evaluator. Return only JSON with a single key 'score'. Use 1.0 if the predicted answer is semantically correct, 0.0 otherwise."
    user = json.dumps({
        "question": question,
        "gold_answer": gold_answer,
        "predicted_answer": predicted_answer,
    }, ensure_ascii=False)
    resp = client.chat.completions.create(
        model=model,
        temperature=0,
        response_format={"type": "json_object"}, # Ép định dạng JSON
        messages=[
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
    )
    txt = resp.choices[0].message.content.strip()
    try:
        obj = json.loads(txt)
        score = float(obj["score"])
        # Nếu score >= 0.5 coi như đúng (trả về 1.0)
        return 1.0 if score >= 0.5 else 0.0
    except Exception:
        return 0.0

