# Kiểm tra thực tế xem trí nhớ được trích xuất có giúp LLM trả lời đúng câu hỏi không (Downstream QA)
def answer_with_retriever(item: Dict[str, Any], retriever_fn) -> Dict[str, Any]:
    q = item["query"]
    chosen = retriever_fn(item) # Chọn 1 trí nhớ
    retrieved_memories = [{
        "memory_id": chosen["memory_id"],
        "text": chosen["text"],
    }]
    # LLM sinh câu trả lời dựa trên trí nhớ đó
    answer = chat_answer(q["query"], retrieved_memories)
    # LLM giám khảo chấm điểm xem trả lời đúng không
    judged = llm_judge_exact(q["query"], q["gold_value"], answer)
    return {
        "query": q["query"],
        "gold_value": q["gold_value"],
        "retrieved_text": chosen["text"],
        "predicted_answer": answer,
        "answer_score": judged,
        "retrieval_correct": int(chosen["memory_id"] == q["gold_memory_id"]),
    }

